    public Main() {
        initComponents();
        getContentPane().setBackground(new Color(63, 109, 217));
          MenuEvent event = new MenuEvent() {
            @Override
            public void menuSelected(int index) {
                switch (index) {
                    case 0:
                        showForm(new FormHome());
                        break;
                    case 1:
                        showForm(new FormMedoc());
                        break;
                    case 2:
                        showForm(new FormEntre());
                        break;
                    case 3:
                        showForm(new FormAchat());
                        break;
                    case 4:
                        showForm(new FormRupture());
                        break;
                    default:
                        showForm(new FormTopVente());
                        break;
                }
                System.out.println("Selected : "+index);
            }
        };
        
        menu.intMenu(event);
        menu.setSelected(0);
    }

     private void showForm(Component com) {
        body.removeAll();
        body.add(com);
        body.revalidate();
        body.repaint();
    }

